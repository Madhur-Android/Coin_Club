package com.example.coinclubapp.Response;

public class CommissionClubCloseResponse {

    private int id;
    private String clubclose;
    private String commission;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getClubclose() {
        return clubclose;
    }

    public void setClubclose(String clubclose) {
        this.clubclose = clubclose;
    }

    public String getCommission() {
        return commission;
    }

    public void setCommission(String commission) {
        this.commission = commission;
    }
}
